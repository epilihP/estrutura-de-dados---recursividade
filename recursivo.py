# EX 1 FATORIAL
def fatorial_recursivo(fatorial):

    if fatorial == 0 or fatorial ==1:
        return 1

    return fatorial  * fatorial_recursivo(fatorial -1)
    

# EX 2 SOMA

def soma_list_recursivasso(lista):
    if not lista == list:
        return 0
    
    return 0 + soma_list_recursivasso[1:]

